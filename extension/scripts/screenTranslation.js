// Iterate through each translation object

function getLocalizedText(textObject) {
    if (textObject.hasOwnProperty(app_language)) {
        return textObject[app_language];
    } else if (textObject.hasOwnProperty("en")) {
        return textObject.en;
    } else {
        return textObject;
    }
}

function translateScreens() {
   
    screenTranslations.forEach((translation) => {
        
        const elements = document.querySelectorAll(translation.selector);
        
        elements.forEach((element) => {
            if (translation.hasOwnProperty("attrName")) {
                element.setAttribute(translation.attrName, getLocalizedText(translation.attrVal));
            } else {
                element.innerHTML = getLocalizedText(translation.text);
            }
        });
        
    });
}
